                    $res = $WxFansModel::where($this->where)
                        ->where('openid',$v['openid'])
                        ->where('fans_id',$fans['fans_id'])
                        ->update($v);
                    if($res){
                        Db::rollback();
                    }
                }else{
                    $v['store_id'] = $this->store_id;
                    $v['update_time'] = time();
                    $v['nickname_encode'] = json_encode($v['nickname']);
                    $WxFansModel::create($v);
                }
